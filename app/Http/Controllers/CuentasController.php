     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $plan = \App\Models\Cuenta::where('id',$request->cuenta_id)->first();
        $detail = new \App\Models\DetalleCuenta;
        $detail->cuenta_id = $plan->id;
        $detail->code = $request->code;
        $detail->name= $request->name;
        $detail->save();
        return redirect()->back()->with(['message' => 'Cuenta agregada exitosamente.', 'alert-type' => 'success']);
    }

